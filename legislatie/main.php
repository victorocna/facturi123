<div class="span-23 box-main">
	<div class="span-22 text-l">Articolul 155 din Codul Fiscal 2007</div>
	<div class="span-22 text-m">(1) Persoana impozabila care efectueaza o livrare de bunuri sau o prestare de servicii, alta decat o livrare/prestare fara drept de deducere a taxei, conform art 141 alin (1) si (2), trebuie sa emita o factura catre fiecare beneficiar, cel tarziu pana in cea de-a 15-a zi lucratoare a lunii urmatoare celei in care ia nastere faptul generator al taxei, cu exceptia cazului in care factura a fost deja emisa. De asemenea, persoana impozabila trebuie sa emita o factura catre fiecare beneficiar, pentru suma avansurilor incasate in legatura cu o livrare de bunuri sau o prestare de servicii, cel tarziu pana in cea dea 15-a zi lucratoare a lunii urmatoare celei in care a incasat avansurile, cu exceptia cazului in care factura a fost deja emisa.</div>
	<div class="span-22 text-m">(2) Persoana inregistrata conform art 153 trebuie sa autofactureze, in termenul prevazut la alin (1), fiecare livrare de bunuri sau prestare de servicii catre sine.</div>
	<div class="span-22 text-m">(3) Persoana impozabila va emite o factura, in termenul stabilit la alin (1), pentru fiecare vanzare la distanta pe care a efectuat-o,in conditiile art 132 alin (2).</div>
	<div class="span-22 text-m">(4) Persoana impozabila va emite o autofactura, in termenul stabilit la alin (1), pentru fiecare transfer pe care la efectuat in alt stat membru, in conditiile art 128 alin (10).</div>
	<div class="span-22 text-l" style="font-size: 1.2em;">(5) Factura cuprinde in mod obligatoriu urmatoarele informatii:</div>
	<div class="span-22 text-m">
		<ol class="text-list" style="list-style-type: lower-alpha;">
			<li>numarul de ordine, in baza uneia sau mai multor serii, care identifica factura in mod unic</li>
			<li>data emiterii facturii</li>
			<li>denumirea/numele, adresa si codul de inregistrare prevazut la art 153, dupa caz, ale persoanei impozabile care emite factura</li>
			<li>denumirea/numele, adresa si codul de inregistrare prevazut la art 153, ale reprezentantului fiscal, in cazul in care furnizorul sau prestatorul nu este stabilit in Romania si si-a desemnat un reprezentant fiscal, daca acesta din urma este persoana obligata la plata taxei</li>
			<li>denumirea/numele, adresa si codul de inregistrare prevazut la art 153, ale cumparatorului de bunuri
sau servicii, dupa caz</li>
			<li>numele si adresa cumparatorului, precum si codul de inregistrare in scopuri de TVA, in cazul in care cumparatorul este inregistrat, conform art 153, precum si adresa exacta a locului in care au fost transferate bunurile, in cazul livrarilor intracomunitare de bunuri prevazute la art 143 alin (2) lit d)</li>
			<li>denumirea/numele, adresa si codul de inregistrare prevazut la art 153, ale reprezentantului fiscal, in cazul in care cealalta parte contractanta nu este stabilita in Romania si si-a desemnat un reprezentant fiscal in Romania, in cazul in care reprezentantul fiscal este persoana obligata la plata taxei</li>
			<li>codul de inregistrare in scopuri de TVA furnizat de client prestatorului, pentru serviciile prevazute la art 133 alin (2) lit c)-f),
lit h) pct 2 si lit i)</li>
			<li>codul de inregistrare in scopuri de TVA cu care se identifica cumparatorul in celalalt stat membru, in cazul operatiunii prevazute la art 143 alin (2) lit a)</li>
			<li>codul de inregistrare in scopuri de TVA cu care furnizorul s-a identificat in alt stat membru si sub care a efectuat achizitia intracomunitara in Romania, precum si codul de inregistrare in scopuri de TVA, prevazut la art 153 sau 153^1, al cumparatorului, in cazul aplicarii art 126 alin (4) lit b)</li>
			<li>denumirea si cantitatea bunurilor livrate, denumirea serviciilor prestate, precum si particularitatile prevazute la art 125^1 alin (3) in definirea bunurilor, in cazul livrarii intracomunitare de mijloace de transport noi</li>
			<li>data la care au fost livrate bunurile/prestate serviciile sau data incasarii unui avans, cu exceptia cazului in care factura este emisa inainte de data livrarii/prestarii sau incasarii avansului</li>
			<li>baza de impozitare a bunurilor si serviciilor, pentru fiecare cota, scutire sau operatiune netaxabila, pretul unitar, exclusiv taxa, precum si rabaturile, remizele, risturnele si alte reduceri de pret</li>
			<li>indicarea, in functie de cotele taxei, a taxei colectate si a sumei totale a taxei colectate, exprimate in lei,
sau a urmatoarelor mentiuni:
				<ol class="text-list">
					<li>in cazul in care nu se datoreaza taxa, o mentiune referitoare la prevederile aplicabile din acest titlu sau din Directiva a 6-a, ori mentiunile "scutit cu drept de deducere", "scutit fara drept de deducere", "neimpozabila in Romania" sau, dupa caz, "neinclus in baza de impozitare"</li>
					<li>in cazul in care taxa se datoreaza de beneficiar in conditiile art 150 alin (1) lit b)-d) si g) sau ale art 160, o mentiune la prevederile prezentului titlu sau ale Directivei a 6-a ori mentiunea "taxare inversa"</li>
				</ol>
			</li>
			<li>o referire la alte facturi sau documente emise anterior, atunci cand se emit mai multe facturi sau documente pentru aceeasi operatiune</li>
			<li>orice alta mentiune ceruta de acest titlu</li>
		</ol>
	</div>
	<div class="span-22 text-l" style="font-size: 1.2em;">(6) Semnarea si stampilarea facturilor nu sunt obligatorii.</div>
	<div class="span-22 text-m">(7) Prin derogare de la alin (1) si fara sa contravina prevederilor alin (3), persoana impozabila este scutita de obligatia emiterii facturii, pentru urmatoarele operatiuni, cu exceptia cazului in care beneficiarul solicita factura:</div>
	<div class="span-22 text-m">
		<ol class="text-list" style="list-style-type: lower-alpha;">
			<li>transportul persoanelor cu taximetre, precum si transportul persoanelor pe baza biletelor de calatorie sau a abonamentelor</li>
			<li>livrarile de bunuri prin magazinele de comert cu amanuntul si prestarile de servicii catre populatie, consemnate in documente, fara nominalizarea cumparatorului</li>
			<li>livrarile de bunuri/prestarile de servicii consemnate in documente specifice, care contin cel putin informatiile prevazute la alin (5)</li>
		</ol>
	</div>
	<div class="span-22 text-m">(8) Prin norme se stabilesc conditiile in care:</div>
	<div class="span-22 text-m">
		<ol class="text-list" style="list-style-type: lower-alpha;">
			<li>se poate intocmi o factura centralizatoare pentru mai multe livrari separate de bunuri sau prestari separate de servicii</li>
			<li>se pot emite facturi de catre cumparator sau client in numele si in contul furnizorului/prestatorului</li>
			<li>se pot transmite facturi prin mijloace electronice</li>
			<li>se pot emite facturi de catre un tert in numele si in contul furnizorului/prestatorului</li>
			<li>se pot pastra facturile intr-un anumit loc</li>
		</ol>
	</div>
	<div class="span-22 text-m">(9) Prin derogare de la dispozitiile alin. (5), se pot intocmi facturi simplificate in cazurile stabilite prin norme.</div>
	
	
	
